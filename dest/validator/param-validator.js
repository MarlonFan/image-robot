var ResJson = require('../modules/core/res-json');

var ParamValidator = (function () {
    function ParamValidator() {
    }
    return ParamValidator;
})();

ParamValidator.prototype.isNumber = function (param, name, res) {
	if (isNaN(param)) {
		res.json(ResJson.failedJson(name + ' is not a number'));
		return false;
	}
	return true;
}

ParamValidator.prototype.isString = function (param, name, res) {
	if (!(typeof param == 'string') || !param) {
		res.josn(ResJson.failedJson(name + ' is not a string'));
		return false;
	}
	return true;
}

